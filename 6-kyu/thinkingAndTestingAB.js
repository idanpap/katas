function testIt(a, b) {
  let sumA = 0;
  let sumB = 0;
  for (number of a.toString()) {
    sumA += parseInt(number);
  }
  for (number of b.toString()) {
    sumB += parseInt(number);
  }
  return sumA * sumB;
}

testIt(12, 34);
